  // Helper method to create Drug objects
  Drug _createDrug({
    required String id,
    required String name,
    required String category,
    required DrugType type,
    double concentrationMg = 0,
    double concentrationMl = 0,
    double minDosePerKg = 0,
    double maxDosePerKg = 0,
    required String description,
    required String warning,
    required String howToUse,
    String? fixedDose,
  }) {
    // Extract Arabic and English names
    String arabicName = name;
    String englishName = name;
    
    if (name.contains('/')) {
      final parts = name.split('/');
      englishName = parts.first.trim();
      arabicName = parts.last.trim();
    }
    
    return Drug(
      id: id,
      name: name,
      arabicName: arabicName,
      englishName: englishName,
      category: category,
      type: type,
      concentrationMg: concentrationMg,
      concentrationMl: concentrationMl,
      minDosePerKg: minDosePerKg,
      maxDosePerKg: maxDosePerKg,
      description: description,
      warning: warning,
      howToUse: howToUse,
      fixedDose: fixedDose,
    );
  }
