  @override
  Future<List<Drug>> getAllDrugs() async {
    return [
      ..._painRelievers(),
      ..._antibiotics(),
      ..._respiratory(),
      ..._gastrointestinal(),
      ..._allergyAndSkin(),
      ..._vitaminsAndSupplements(),
      ..._chronicAndOthers(),
    ];
  }

  @override
  Future<List<Drug>> searchDrugs(String query) async {
    final allDrugs = await getAllDrugs();
    final lowerQuery = query.toLowerCase();
    
    return allDrugs.where((drug) {
      return drug.searchKey.toLowerCase().contains(lowerQuery);
    }).toList();
  }

  @override
  Future<Drug?> getDrugById(String id) async {
    final allDrugs = await getAllDrugs();
    try {
      return allDrugs.firstWhere((drug) => drug.id == id);
    } catch (e) {
      return null;
    }
  }

  @override
  Future<List<Drug>> getDrugsByCategory(String category) async {
    final allDrugs = await getAllDrugs();
    return allDrugs.where((drug) => drug.category == category).toList();
  }

  @override
  Future<List<String>> getAllCategories() async {
    final allDrugs = await getAllDrugs();
    final categories = allDrugs.map((drug) => drug.category).toSet();
    return categories.toList();
  }

